'<h2>Programme and Abstracts</h2>\n'...
'<p>Generated automatically</p>\n'...
'</div>\n']);

% ---------------- TALKS ----------------

for k = 1:numel(posters)

    t = posters(k);

    fprintf(fid,'<div class="talk">\n');

    % Header
    fprintf(fid,...
    ['<div class="header">' ...
     '<span class="day">%s</span>' ...
     '<span class="time">%s</span><br>' ...
     '<span class="topic">%s</span>' ...
     '</div>\n'], ...
     escapeHTML(t.Day), ...
     escapeHTML(t.Time), ...
     escapeHTML(t.Topic));

    % Title
    fprintf(fid,...
        '<div class="title">%s</div>\n',...
        escapeHTML(t.Title));


    % Authors

    fprintf(fid,'<div class="authors">');

    if isfield(t,'Authors') && ~isempty(t.Authors)

        for a = 1:length(t.Authors)

            if a > 1
                fprintf(fid,', ');
            end

            fprintf(fid,'%s',...
                escapeHTML(t.Authors(a).name));

        end

    elseif isfield(t,'Author')

        fprintf(fid,'%s',escapeHTML(t.Author));

        if isfield(t,'CoAuthors')
            fprintf(fid,', %s',escapeHTML(t.CoAuthors));
        end
    end

    fprintf(fid,'</div>\n');


    % Affiliations

    if isfield(t,'Affiliations') && ~isempty(t.Affiliations)

        fprintf(fid,'<div class="aff">');

        for a = 1:length(t.Affiliations)

            fprintf(fid,'<sup>%d</sup> %s<br>',...
                a,...
                escapeHTML(t.Affiliations{a}));

        end

        fprintf(fid,'</div>\n');

    end


    % Abstract

    fprintf(fid,...
        '<div class="abstract"><h3>Abstract</h3>%s</div>\n',...
        formatAbstract(t.Abstract));


    fprintf(fid,...
        '<div class="footer">Talk %d</div>\n',...
        k);

    fprintf(fid,'</div>\n');

end


