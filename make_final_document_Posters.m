clear all
close all
txt = fileread('posters_programme.html');

% Find the start of the line "const favs=new"

% Extract everything between <script> and "const favs=new"
tok = regexp(txt, '<script>\s*(.*?)\s*(?=const favs=new)', ...
    'tokens', 'once');
txt = tok{1};
% Remove JavaScript wrapper
txt = erase(txt, "const posters=");
txt = erase(txt, ";");

objects = regexp(txt, '\{(.*?)\}', 'tokens');

posters = struct([]);

for i = 1:length(objects)

    obj = objects{i}{1};

    % Extract fields
    posters(i).Day = getField(obj, 'Day');
    posters(i).Time = getField(obj, 'Time');
    posters(i).Topic = getField(obj, 'Topic');
    
    posters(i).Title = getField(obj, 'Title');
    posters(i).Speaker = getField(obj, 'Author');
    posters(i).CoAuthors = getField(obj, 'CoAuthors');
    posters(i).Abstract = getField(obj, 'Abstract');

    % Numeric field
    posters(i).SubmissionID = str2double(getField(obj, 'SubmissionID'));

end
% Create a nice HTML booklet from talk/poster structure
% Input:
% posters = array of structures with fields:
% Day, Time, Topic, Title, Authors, Affiliations, Abstract



% ---------------- USER DATA ----------------
% posters should already exist in workspace
% Example:
% load('talks.mat')

outputFile = 'TalkBooklet.html';
delete(outputFile)
% ---------------- HTML HEADER ----------------

